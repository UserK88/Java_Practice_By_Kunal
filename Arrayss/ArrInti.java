package Arrayss;

public class ArrInti {
    public static void main(String [] args){
    int[] intarr = {1,2,3,4,5,6};
    for(int a : intarr){
        System.out.println(a);
    }
    byte[] bytearr = {1,2,3,4,5,6};
    for(byte a : bytearr){
        System.out.println(a);
    }
    short[] shortarr = {1,2,3,4,5,6};
    for(short a : shortarr){
        System.out.println(a);
    }
    long[] longarr = {1,2,3,4,5,6};
    for(long a : longarr){
        System.out.println(a);
    }
    float[] floatarr = {1,2,3,4,5,6};
    for(float a : floatarr){
        System.out.println(a);
    }
    double[] doublearr = {1,2,3,4,5,6};
    for(double a : doublearr){
        System.out.println(a);
    }
    char[] chararr = {'a','b'};
    for(char a : chararr){
        System.out.println(a);
    }

    }
}
