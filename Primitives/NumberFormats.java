
    public class NumberFormats {
    public static void main(String[] args) {
        // Initializing numbers in different formats
        int decimalNumber = 100;       // Decimal format
        int octalNumber = 0144;        // Octal format (prefix 0)
        int hexNumber = 0x64;          // Hexadecimal format (prefix 0x)
        
        System.out.println("Decimal number: " + decimalNumber);
        System.out.println("Octal number (0144): " + octalNumber);
        System.out.println("Hexadecimal number (0x64): " + hexNumber);
    }
}

