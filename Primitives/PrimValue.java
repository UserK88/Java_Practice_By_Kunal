public class PrimValue {
    byte by;
    int i;
    float f;
    char ch;
    long l;
    double v;
    short sh;
    boolean bl;


    public static void main(String [] args){
        PrimValue pm = new PrimValue();
        System.out.println(pm.by);
        System.out.println(pm.sh);
        System.out.println(pm.i);
        System.out.println(pm.l);
        System.out.println(pm.f);
        System.out.println(pm.v);
        System.out.println(pm.ch);
        System.out.println(pm.bl);        
    }
}
